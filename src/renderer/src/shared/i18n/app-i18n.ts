export type AppI18nKey = keyof (typeof APP_I18N_MESSAGES)['zh-CN']

type AppTranslator = (key: AppI18nKey, params?: AppI18nParams) => string

function interpolateTemplate(template: string, params?: AppI18nParams): string {
  if (!params) {
    return template
  }

  return template.replace(/\{\{\s*([\w.-]+)\s*\}\}/g, (_match, key: string) => {
    const value = params[key]
    return value === null || value === undefined ? '' : String(value)
  })
}

export function translateAppMessage(
  language: AppLanguage,
  key: AppI18nKey,
  params?: AppI18nParams
): string {
  const resolvedLanguage = normalizeAppLanguage(language)
  const value = APP_I18N_MESSAGES[resolvedLanguage][key] ?? APP_I18N_MESSAGES['zh-CN'][key]

  return interpolateTemplate(value, params)
}

export function createAppTranslator(language: AppLanguage): AppTranslator {
  return (key, params) => translateAppMessage(language, key, params)
}

export function useAppI18n(): {
  language: AppLanguage
  t: AppTranslator
} {
  const language = useAppStore((state) => normalizeAppLanguage(state.preferences.language))

  const t = useMemo(() => createAppTranslator(language), [language])

  return {
    language,
    t
  }
}

export function getCurrentAppLanguage(): AppLanguage {
  return normalizeAppLanguage(useAppStore.getState().preferences.language)
}

export function translateWithAppLanguage(key: AppI18nKey, params?: AppI18nParams): string {
  const language = getCurrentAppLanguage()
  return translateAppMessage(language, key, params)
}
